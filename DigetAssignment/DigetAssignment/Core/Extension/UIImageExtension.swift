        static let moonFill = UIImage(systemName: "moon.fill")!
        static let sunMaxFill = UIImage(systemName: "sun.max.fill")!
        static let personCircle = UIImage(systemName: "person.circle")!
        static let heartTextSquare = UIImage(systemName: "heart.text.square")!
        static let personCircleFill = UIImage(systemName: "person.circle.fill")!
        static let calendarBadgeClock = UIImage(systemName: "calendar.badge.clock")!
        static let heartTextSquareFill = UIImage(systemName: "heart.text.square.fill")!
    }
    
 
    
    
}
